            <p class="text-lg mb-6">Clarence Bowls Club has a rich and storied history, dating back over a century. Founded in 1907, the club has been a cornerstone of the local sporting community for generations.</p>

            <h2 class="text-2xl font-bold mb-4">The Early Years</h2>
            <p>Clarence Bowling Club was founded in 1907, followed in 1926 by Clarence Ladies’ Bowling Club. In 1965, Wessex Ladies’ Bowling Club was formed, and continued to share our facilities.</p>

            <h2 class="text-2xl font-bold mt-8 mb-4">Growth and Development</h2>
            <p>In 2002, following a generous legacy from former club-member Les Andrews – and a great deal of hard work by our members – the Les Andrews pavilion was opened by David Bryant MBE and this provides us with one of the best clubhouse facilities in the country.</p>

            <h2 class="text-2xl font-bold mt-8 mb-4">Further Growth</h2>
            <p>In 2017, Clarence Ladies joined Clarence Bowling Club to form a mixed club – whilst Wessex Ladies voted to remain independent.</p>
            <p>In March 2023 Wessex Ladies Bowling Club decided to wind up and Clarence Bowling Club has continued as the sole occupiers of the Clarence Park facility with the greens maintained by Avon Sportsground Maintenance who, under the supervision of our Greens Manager, maintain our three greens to a very high standard.</p>
